package com.gwamcc.aii.forms;

import com.gwamcc.aii.util.sql2.anno.PK;
import com.gwamcc.aii.util.sql2.anno.Table;

/**
 * 数据库名称对象
 * @author 张亚平
 *
 */
@Table("T_DataBase")
public class DbNameForm  extends DefaultForm{
	@PK
	private int id;//数据库id
	private String name;//数据库名称
	public DbNameForm() {	}
	public int getId() {
		return id;
	}
	public DbNameForm setId(int id) {
		this.id = id;
		return this;
	}
	public String getName() {
		return name;
	}
	public DbNameForm setName(String name) {
		this.name = name;
		return this;
	}

}
