package com.gwamcc.aii.forms;

import com.gwamcc.aii.util.sql2.anno.PK;
import com.gwamcc.aii.util.sql2.anno.Table;

/**
 * 数据库对象名称对象
 * @author 张亚平
 *
 */
@Table("T_DataBaseObj")
public class DbObjNameForm  extends DefaultForm{
	@PK
	private int id;//数据库对象id
	private String objName;//数据库对象名称
	public DbObjNameForm() {	}
	public int getId() {
		return id;
	}
	public DbObjNameForm setId(int id) {
		this.id = id;
		return this;
	}
	public String getObjName() {
		return objName;
	}
	public DbObjNameForm setObjName(String objName) {
		this.objName = objName;
		return this;
	}

}
