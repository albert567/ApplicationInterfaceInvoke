package com.gwamcc.aii.forms;

import java.util.List;

/**
 * 应用功能源码关联数据模型
 * @author 张亚平
 *
 */
public class AppCodeTreeForm extends DefaultForm{
	private int id;
	private String text;
	private String state;
	private boolean checked=false;
	private List<DefaultForm> children;
	public int getId() {
		return id;
	}
	public AppCodeTreeForm setId(int id) {
		this.id = id;
		return this;
	}
	public String getText() {
		return text;
	}
	public AppCodeTreeForm setText(String text) {
		this.text = text;
		return this;
	}
	public String getState() {
		return state;
	}
	public AppCodeTreeForm setState(String state) {
		this.state = state;
		return this;
	}
	public List<DefaultForm> getChildren() {
		return children;
	}
	public AppCodeTreeForm setChildren(List<DefaultForm> children) {
		this.children = children;
		return this;
	}
	public boolean getChecked() {
		return checked;
	}
	public AppCodeTreeForm setChecked(boolean checked) {
		this.checked = checked;
		return this;
	}
}
