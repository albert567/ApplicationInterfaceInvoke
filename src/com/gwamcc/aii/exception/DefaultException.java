package com.gwamcc.aii.exception;

/**
 * 默认异常
 * @author 范大德
 *
 */
public abstract class DefaultException extends Exception {

	public static String DEFAULT_VIEW="/exception/error";//默认异常页面
	/**
	 *
	 */
	private static final long serialVersionUID = 1L;



	public DefaultException() {
		super();
	}



	public DefaultException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}



	public DefaultException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}



	public DefaultException(String arg0) {
		super(arg0);
	}



	public DefaultException(Throwable arg0) {
		super(arg0);
	}



	/**
	 * 获取异常处理页面
	 * @return
	 */
	public abstract String getView();

}
