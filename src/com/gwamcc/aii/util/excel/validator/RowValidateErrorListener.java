package com.gwamcc.aii.util.excel.validator;

import com.gwamcc.aii.util.excel.RowValidator;

/**
 * Excel行验证失败监听器
 * @author 范大德
 *
 */
public interface RowValidateErrorListener {
	/**
	 * 当验证失败时调用的方法
	 * 	如果返回值是false,则终止当前遍历
	 * @param validator
	 * @return
	 * @throws Exception
	 */
	public boolean onError(RowValidator validator) throws Exception;
}
