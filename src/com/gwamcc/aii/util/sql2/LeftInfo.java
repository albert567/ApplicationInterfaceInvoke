package com.gwamcc.aii.util.sql2;

/**
 * 左连接信息
 * @author 范大德
 *
 */
public class LeftInfo {
	//连接表
	private String tableName;
	//连接字段
	private String onField;
	//查询字段
	private String[]field;
	private int seq=1;
	public String getTableName() {
		return tableName;
	}
	public LeftInfo setTableName(String tableName) {
		this.tableName = tableName;
		return this;
	}
	public String getOnField() {
		return onField;
	}
	public LeftInfo setOnField(String onField) {
		this.onField = onField;
		return this;
	}
	public String[] getField() {
		return field;
	}
	public LeftInfo setField(String[] field) {
		this.field = field;
		return this;
	}
public int getSeq() {
	return seq;
}
public LeftInfo setSeq(int seq) {
	this.seq = seq;
	return this;
}
}
